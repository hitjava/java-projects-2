package CURIE;

public class switch_case {

	
	public static void main(String[] args) {
		int ch = 2;
		switch(ch){
		case 1:
		{
		System.out.println("java");
		break;
		}
		case 2:{
		System.out.println("programs");
		break;
		}
		default:{
		System.out.println("Not Listed");
	}
		
	}	

	}
}

